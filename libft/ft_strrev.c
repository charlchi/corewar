#include "libft.h"

void	ft_strrev(char **str)
{
	char	*s;
	char	t;
	int		i;
	int		len;

	s = *str;
	len = ft_strlen(*str) - 1;
	i = 0;
	while (i < (len / 2) + 1)
	{
		t = s[i];
		s[i] = s[len - i];
		s[len - i] = t;
		i++;
	}
}
