#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 && s2)
	{
		if (n == 0)
			return (1);
		while (n--)
			if (s1[n] != s2[n])
				return (0);
		return (1);
	}
	return (0);
}
