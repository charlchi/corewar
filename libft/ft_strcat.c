#include "libft.h"

char		*ft_strcat(char *dest, const char *src)
{
	int s;
	int d;

	s = 0;
	d = 0;
	while (dest[d])
		d++;
	while ((dest[d] = src[s]))
	{
		d++;
		s++;
	}
	return (dest);
}
